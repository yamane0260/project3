	function getLang() {
		const url = new URL(window.location.href);
		const q = url.searchParams.get('lang');
		if (q && SUPPORTED.includes(q)) {
			try { localStorage.setItem(STORAGE_KEY, q); } catch(_) {}
			return q;
		}
		try {
			const saved = localStorage.getItem(STORAGE_KEY);
			if (saved && SUPPORTED.includes(saved)) return saved;
		} catch(_) {}
		return DEFAULT_LANG;
	}

	function setLang(lang) {
		if (!SUPPORTED.includes(lang)) return;
		try { localStorage.setItem(STORAGE_KEY, lang); } catch(_) {}
		applyI18n(lang);
	}

	function t(key) {
		const lang = I18N.lang || getLang();
		const dict = translations[lang] || {};
		return (key in dict) ? dict[key] : (translations[DEFAULT_LANG][key] || key);
	}

	function applyI18n(lang) {
		I18N.lang = lang || getLang();
		// data-i18n="key" â†’ textContent
		document.querySelectorAll('[data-i18n]').forEach(node => {
			const key = node.getAttribute('data-i18n');
			if (!key) return;
			const value = t(key);
			if (node.tagName === 'INPUT' && (node.type === 'button' || node.type === 'submit')) {
				node.value = value;
			} else if (node.tagName === 'INPUT' && node.type === 'range') {
				// noop
			} else {
				node.textContent = value;
			}
		});
		// data-i18n-html="key" â†’ innerHTML
		document.querySelectorAll('[data-i18n-html]').forEach(node => {
			const key = node.getAttribute('data-i18n-html');
			if (!key) return;
			node.innerHTML = t(key);
		});
		// data-i18n-attr="attrName:key"
		document.querySelectorAll('[data-i18n-attr]').forEach(node => {
			const spec = node.getAttribute('data-i18n-attr');
			if (!spec) return;
			const [attr, key] = spec.split(':');
			if (!attr || !key) return;
			node.setAttribute(attr, t(key));
		});
		// html lang
		const html = document.documentElement;
		if (html) html.setAttribute('lang', I18N.lang);
	}

	// expose
	window.I18N = { t, setLang, getLang, applyI18n, translations, SUPPORTED };

	document.addEventListener('DOMContentLoaded', function(){
		applyI18n(getLang());
	});
